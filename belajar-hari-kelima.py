# jika kamu ingin membuat beberapa value tapi dalam satu variable, kamu bisa melakukan seperti ini :
x, y, z = "Pertama", "Kedua", "Ketiga"
print(x)
print(y)
print(z)

l, i, j = "String", 3, 2.5
print(l)
print(i)
print(j)

# dan kamu juga bisa menyatakan value yang sama dengan beberapa variable
x = y = z = "LOL"
print(x)
print(y)
print(z)

# dan jika kalian membuat variable dengan list, tuple, dictionary
# contoh list
my_list = ["stawberi", "anggur", "jeruk"]
x, y, z = my_list
print(x)
print(y)
print(z)

# contoh tuple 
my_tuple = ("lena", "lesa", "liru")
x, y, z = my_tuple
print(x)
print(y)
print(z)

